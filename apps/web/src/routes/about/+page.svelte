  <h1>About Language of Learning</h1>
  <p>
    Language of Learning helps programming learners see how code builds results — not just read
    lines of text. The product turns supported Python into synchronized diagrams of values,
    decisions, state changes, and returns.
  </p>
  <p>
    Every visual element is derived from deterministic engine artifacts. The system does not use
    AI to guess what your program does.
  </p>
  <h2>Who it is for</h2>
  <ul>
    <li>Beginner and intermediate programming learners</li>
    <li>Teachers and tutors explaining loops and state</li>
    <li>Curriculum designers building pattern-based lessons</li>
  </ul>
